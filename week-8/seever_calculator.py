def add(number1, number2): #function to add two numbers and return the added total.
    return number1 + number2

def subtract(number1, number2): #function to subtract two numbers and return the subtracted total.
    return number1 - number2

def divide(number1, number2): #function to divide two numbers and return the divided total.
    return number1 / number2

# Print the result of each calculator call
print(add(1,2))

print(subtract(4,1))

print(divide(8,2))
